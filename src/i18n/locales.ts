// 获取翻译函数
export const t = (key: string, lang: Language): string => {
  const keys = key.split('.')
  let value: any = translations[lang]
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k]
    } else {
      return key // 返回原key如果找不到翻译
    }
  }
  
  return typeof value === 'string' ? value : key
}
